<h1>Privacyverklaring Pizzeria Sole Machina</h1>

<p>
    Pizzeria Sole Machina gaat zorgvuldig om met jouw persoonsgegevens.
    Op deze pagina leggen wij in begrijpelijke taal uit welke gegevens wij verwerken,
    waarom wij dat doen en welke rechten jij hebt volgens de Algemene Verordening Gegevensbescherming (AVG).
</p>

<hr>

<h2>1. Wie is verantwoordelijk voor de gegevens?</h2>
<p>
    De webapplicatie wordt gebruikt door Pizzeria Sole Machina.
    De pizzeria is verantwoordelijk voor de verwerking van persoonsgegevens in dit systeem.
</p>

<hr>

<h2>2. Welke gegevens verwerken wij?</h2>
<p>Wij verwerken alleen gegevens die nodig zijn om jouw bestelling af te handelen:</p>
<ul>
    <li><strong>Accountgegevens</strong> – gebruikersnaam, wachtwoord (versleuteld), voornaam, achternaam.</li>
    <li><strong>Bestelgegevens</strong> – welke producten je bestelt, aantallen, bestelnummer en datum/tijd.</li>
    <li><strong>Afleveradres</strong> – het adres waar de bestelling bezorgd moet worden.</li>
    <li><strong>Gebruikersrol</strong> – of een account klant of personeelslid is.</li>
</ul>

<p>
    Wachtwoorden worden nooit in leesbare vorm opgeslagen. In de database staat alleen een
    <strong>gehashte</strong> versie van jouw wachtwoord.
</p>

<hr>

<h2>3. Waarvoor gebruiken wij jouw gegevens?</h2>
<p>Wij gebruiken jouw gegevens voor de volgende doelen:</p>
<ul>
    <li>Om een account aan te maken en je te laten inloggen in de webapplicatie.</li>
    <li>Om jouw bestelling op te nemen in het systeem en te verwerken.</li>
    <li>Om jouw afleveradres te gebruiken voor het bezorgen van de bestelling.</li>
    <li>Om jouw eerdere bestellingen te tonen in je profiel.</li>
    <li>Om personeel een overzicht van actieve bestellingen te geven.</li>
</ul>

<p>
    Wij gebruiken jouw gegevens <strong>niet</strong> voor reclame naar derden en we verkopen
    jouw gegevens niet aan andere partijen.
</p>

<hr>

<h2>4. Bewaartermijn</h2>
<p>
    Bestellingen en accountgegevens worden bewaard zolang het account actief is en
    zolang de administratie dat vereist. Gegevens worden niet langer bewaard dan
    noodzakelijk is voor het doel waarvoor ze zijn verzameld.
</p>

<hr>

<h2>5. Cookies en sessies</h2>
<p>
    De webapplicatie maakt gebruik van een <strong>sessie-cookie</strong> om jou
    ingelogd te houden tijdens het gebruik van de site. Dit cookie bevat geen
    wachtwoord of gevoelige gegevens, maar een willekeurige sessiecode.
</p>
<p>
    Zodra je de browser sluit of uitlogt via <code>loguit.php</code>,
    wordt de sessie beëindigd.
</p>

<hr>

<h2>6. Beveiliging</h2>
<p>Wij nemen passende maatregelen om jouw gegevens te beveiligen, onder andere:</p>
<ul>
    <li>Wachtwoorden worden opgeslagen met <strong>password hashing</strong>.</li>
    <li>Databaseacties gebruiken <strong>prepared statements</strong> om SQL-injectie te voorkomen.</li>
    <li>Gevoelige gegevens worden alleen getoond aan ingelogde gebruikers met de juiste rol.</li>
</ul>

<hr>

<h2>7. Jouw rechten (AVG)</h2>
<p>Volgens de AVG heb je een aantal privacyrechten, zoals:</p>
<ul>
    <li>Recht op inzage – je mag zien welke gegevens wij van jou hebben.</li>
    <li>Recht op rectificatie – je mag onjuiste gegevens laten aanpassen.</li>
    <li>Recht op verwijdering – in sommige gevallen mag je vragen om gegevens te verwijderen.</li>
</ul>

<p>
    Als je vragen hebt over jouw gegevens of gebruik wilt maken van één van deze rechten,
    kun je contact opnemen met de pizzeria.
</p>

